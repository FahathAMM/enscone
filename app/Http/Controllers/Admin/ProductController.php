     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $items = $this->repo->createWithAlertMessage($request->except('product_image'), ['status', 'Stored was successful']);
            $this->repo->imageUpload($items, 'products', $request->product_image);
            return  redirect(route('products.index'));
        } catch (\Throwable $th) {
            throw $th;
        }
    }

